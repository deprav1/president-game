/**
 * Определяет финал по доминирующей шкале и сроку правления.
 */
export function getVictoryEnding(stats, months) {
  // Особый финал: очень долгое сбалансированное правление
  if (months >= 120 && Object.values(stats).every(v => v >= 38 && v <= 76)) {
    return ENDINGS.legenda;
  }

  // Паранойя: долгое правление при подавленном народе
  if (months >= 96 && stats.people < 35) return ENDINGS.bunker;

  // Экономический крах: олигархи разорены
  if (stats.oligarchs < 20) return ENDINGS.default_end;

  // Дворцовый переворот: армия сильна, но народ подавлен
  if (stats.army >= 68 && stats.people < 30) return ENDINGS.coup;

  // Доминирующая фракция
  if (stats.army >= 68)     return ENDINGS.oprichnina;
  if (stats.oligarchs >= 68) return ENDINGS.kooperativ;
  if (stats.west >= 68)     return ENDINGS.perestroika;

  // Эмиграция элит: Запад высокий, олигархи бегут
  if (stats.west >= 58 && stats.oligarchs < 32) return ENDINGS.emigration;

  // Теократия: народ лоялен, но никто не доминирует
  if (stats.people >= 68) return ENDINGS.holy;

  // Нет явного победителя — застой
  return ENDINGS.zastoy;
}
